package com.nutricore.manager.domain.enums.goal;

/**
 * Representa qual é o objetivo clínico, não o método.
 */
public enum GoalType {

    WEIGHT_LOSS, // Redução de peso corporal
    FAT_LOSS, // Redução de percentual de gordura
    MUSCLE_GAIN, // Ganho de massa muscular
    WEIGHT_MAINTENANCE, // Manutenção do peso atual
    BODY_RECOMPOSITION // Redução de gordura + ganho de massa
}
